package Global;

public class DataStruct
{
	private int wendu=0;
	private int shidu=0;
	private int guangzhao=0;
	
	public DataStruct(int wendu,int shidu,int guangzhao)
	{
		this.wendu=wendu;
		this.shidu=shidu;
		this.guangzhao=guangzhao;
	}

	public int getWendu() {
		return wendu;
	}

	public void setWendu(int wendu) {
		this.wendu = wendu;
	}

	public int getShidu() {
		return shidu;
	}

	public void setShidu(int shidu) {
		this.shidu = shidu;
	}

	public int getGuangzhao() {
		return guangzhao;
	}

	public void setGuangzhao(int guangzhao) {
		this.guangzhao = guangzhao;
	}
	
	
}
